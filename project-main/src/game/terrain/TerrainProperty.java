package game.terrain;

/**
 * This Enum class is for Terrain's with a specific property
 * @author Ian Lai
 * @version 1.0.0
 */

public enum TerrainProperty {
    /**
     * An Attribute for Terrain's that cannot be set on Fire
     */
    NON_BURNABLE,
    /**
     * An Attribute for Terrain's that set with Water
     */
    BODY_OF_WATER
}
